package clases;

/**
 * @author Luis Tapia
 */
public class Persona {
    private String Nombre;
    private short Edad;
    private char Sexo;

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    public short getEdad() {
        return Edad;
    }

    public void setEdad(short Edad) {
        this.Edad = Edad;
    }

    public char getSexo() {
        return Sexo;
    }

    public void setSexo(char Sexo) {
        this.Sexo = Sexo;
    }   
    
    
}
